	COMP_TEL	NVARCHAR(150),
	COMP_FAX	NVARCHAR(150),
	COMP_MAIL	NVARCHAR(150),
	IS_ACT	BIT,
	DO_TIME	NVARCHAR(150),
	DO_USER	NVARCHAR(50)
)
